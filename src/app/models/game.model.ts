/**
 * Game class
 */
export class Game {
  /**
   * string id that identify the game
   */
  id: string;

  /**
   * game name to be displayed
   */
  name: string;

  /**
   * background to play component
   */
  background: string;

  /**
   * small image to be displayed
   */
  thumbnail: string;

  /**
   * long game's descriptiono
   */
  description_long: string;

  /**
   * vendor's name
   */
  vendor: string;

  /**
   * creation's date
   */
  creation: string;

  /**
   * rating number
   */
  rating: number;
}
